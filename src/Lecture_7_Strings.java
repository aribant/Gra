
public class Lecture_7_Strings {

	public static void main(String[] args) {  // learned the keyboard short for
											 // generating main method 
											// (ctrl space)

		int myInt = 7;

		String text = "Hello";   // String is a class. text is a variable. "hello"
								// is a text object.

		String blank = " ";

		String name = "Bob";

		String greeting = text + blank + name; // adding the strings together.

		System.out.println(greeting);

		System.out.println("Hello" + " " + "Bob"); // Concatenation

		System.out.println("My integer is: " + myInt); // text plus variable.

		double myDouble = 7.8;

		System.out.println("My number is: " + myDouble + ".");

	}

}
