
public class Lecture_9_For_Loops {

	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) { // The variable i is set to a value of 0.
										// Infinite Loop while i is less than 5.
										// Every time the loop iterates add one
										// to i.
			System.out.printf("The value of i is: %d\n", i); // This line prints
																// out the value
																// of i each
																// time the loop
																// runs.
		}

	}
}
