
public class Lecture_8_While_Loops {

	public static void main(String[] args) {

		int value = 0;

		while (value < 10) { // while the value of value is less
								// than 10, the loop repeats.

			System.out.println("Hello");

			value = value + 1; // + 1 adds 1 to value each time
								// the loop is run. when value reaches 10
								// the loop condition is false and
								// the loop stops.
		}

	}
}
